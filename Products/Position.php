<?php

trait Position {
  public $scaffale;
  public $mensola;


  public function getScaffale() {
    return $this->scaffale;
  }

  public function setScaffale($valore) {
    $this->scaffale = $valore; 
  }

  public function getMensola() {
    return $this->mensola;
  }

  public function setMensola($valore) {
    $this->mensola = $valore; 
  }
  
  
}

 
  

